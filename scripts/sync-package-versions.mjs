#!/usr/bin/env node
// Regenerate packages/*/src/version.ts so SDK_VERSION matches package.json.
// Skips packages marked private:true.
import { readdirSync, readFileSync, writeFileSync, existsSync, mkdirSync } from 'node:fs';
import { join } from 'node:path';

const root = new URL('..', import.meta.url).pathname;
const packagesDir = join(root, 'packages');

const HEADER = '// Auto-generated by scripts/sync-package-versions.mjs.\n// Do not edit by hand. Run `pnpm sync:versions` to refresh.\n';

let touched = 0;
for (const name of readdirSync(packagesDir).sort()) {
  const pkgRoot = join(packagesDir, name);
  const pkgJsonPath = join(pkgRoot, 'package.json');
  if (!existsSync(pkgJsonPath)) continue;
  const meta = JSON.parse(readFileSync(pkgJsonPath, 'utf8'));
  if (meta.private === true) continue;
  if (!meta.name || !meta.version) continue;

  const srcDir = join(pkgRoot, 'src');
  if (!existsSync(srcDir)) mkdirSync(srcDir, { recursive: true });

  const out = `${HEADER}export const SDK_NAME = ${JSON.stringify(meta.name)};\nexport const SDK_VERSION = ${JSON.stringify(meta.version)};\n`;
  const target = join(srcDir, 'version.ts');
  const existing = existsSync(target) ? readFileSync(target, 'utf8') : '';
  if (existing !== out) {
    writeFileSync(target, out);
    touched += 1;
  }
}

console.log(`sync:versions — ${touched} file(s) updated.`);
